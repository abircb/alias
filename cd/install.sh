#!/usr/bin/env bash

# change directories easily
alias ..='cd ../'
echo "added .. : an alias for cd ../"

alias ...='cd ../../'
echo "added ... : an alias for cd ../../"

alias ....='cd ../../..'
echo "added .... : an alias for cd ../../../"

alias .....='cd ../../../..'
echo "added ..... : an alias for cd ../../../../"
